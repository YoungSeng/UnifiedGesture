import argparse
import torch
import models


class Options():

    def __init__(self):
        self.initialized = False

    def initialize(self, parser):
        parser.add_argument('--input_A', required=True, help='path to first bvh file')
        parser.add_argument('--input_B', required=True, help='path to second bvh file')
        parser.add_argument('--model_path', required=True, help='path to second bvh file')
        parser.add_argument('--edit_type', type=str, default='retargeting', help='name of the motion editing operation, retargeting or style_transfer')
        parser.add_argument('--gpu_ids', type=str, default='0', help='gpu ids: e.g. 0  0,1,2, 0,2. use -1 for CPU')

        return parser

    def gather_options(self):

        if not self.initialized:  # check if it has been initialized
            parser = argparse.ArgumentParser(formatter_class=argparse.ArgumentDefaultsHelpFormatter)
            parser = self.initialize(parser)

        # get the basic options
        opt, _ = parser.parse_known_args()

        # modify model-related parser options
        model_name = opt.model
        model_option_setter = models.get_option_setter(model_name)
        parser = model_option_setter(parser, self.isTrain)
